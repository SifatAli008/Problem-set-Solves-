#include <iostream>
using namespace std;

int main(){
    int X;
    cin>>X;
    cout<<"X++ :"<<X++<<endl;
    cout<<"++X :"<<++X<<endl;
    cout<<"X-- :"<<X--<<endl;
    cout<<"--X :"<<--X<<endl;
}